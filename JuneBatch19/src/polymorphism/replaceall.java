package polymorphism;

public class replaceall {

	public static void main(String[] args) {
		
		
		String name = "Abhishek";
		
		System.out.println(name.replace('A', 'B'));
		
		String s = "Abhishek is here is ";
		
		System.out.println(s.replaceAll(" is ", " was "));
		

	}

}
